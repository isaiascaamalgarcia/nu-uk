    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
        int cant = 0;
        double importe = 0;
        try {
            cantidad = jSpinner1.getValue().toString();
            cant = Integer.parseInt(cantidad);
            if (cant < 1) {
                JOptionPane.showMessageDialog(null, "Elija minimo un\nproducto.");
                jSpinner1.requestFocus();
            } else {
                importe = cant * (Double.parseDouble(this.importe));
                modelo.setValueAt(cant, fila, 0);
                modelo.setValueAt(importe, fila, 4);
                System.out.println("FILAS   " + this.tabla.getRowCount());
                tabla();
                this.hide();
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnAñadirActionPerformed

