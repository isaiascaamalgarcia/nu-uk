//  Actualiza la información de las ventas.

    public void tabla() {
        String cambioDatos = "";
        double _subtotal = 0, _total = 0, _iva = 0;
        try {
            for (int i = 0; i < this.tabla.getRowCount(); i++) {
                cambioDatos = this.tabla.getValueAt(i, 4).toString();
                System.out.println(cambioDatos);
                _subtotal += Double.parseDouble(cambioDatos);
            }
            subtotal.setText(String.valueOf(_subtotal));
            _iva = _subtotal * 16 / 100;
            iva.setText(String.valueOf(_iva));
            _total = _subtotal + _iva;
            total.setText(String.valueOf(df.format(_total)));
        } catch (Exception e) {
        }

    }

